export function BottomNav({ username: _username }: BottomNavProps) {
  const pathname = usePathname()

  const isActive = (href: string, exact: boolean) =>
    exact ? pathname === href : pathname.startsWith(href)

  return (
    <nav
      className="fixed inset-x-0 bottom-0 z-40 border-t border-white/[0.07] bg-ink-950/95 backdrop-blur-xl lg:hidden"
      aria-label="Mobile navigation"
    >
      {/* Scrollable row — allows more items without crowding */}
      <div className="flex overflow-x-auto scrollbar-none [-webkit-overflow-scrolling:touch] px-1">
        {NAV_ITEMS.map((item) => {
          const active = isActive(item.href, item.exact)
          return (
            <Link
              key={item.href}
              href={item.href}
              aria-current={active ? 'page' : undefined}
              className={cn(
                'relative flex flex-col items-center gap-1 py-2.5 px-3 text-[10px] font-medium transition-colors duration-150 flex-shrink-0 min-w-[60px]',
                active ? 'text-gold-400' : 'text-white/35 hover:text-white/60',
              )}
            >
              {/* Active top indicator */}
              <span
                aria-hidden
                className={cn(
                  'absolute top-0 left-1/2 h-0.5 w-6 -translate-x-1/2 rounded-full bg-gold-500 transition-opacity duration-150',
                  active ? 'opacity-100' : 'opacity-0',
                )}
              />
              {item.icon}
              <span className="leading-none">{item.label}</span>
            </Link>
          )
        })}
      </div>

      {/* Safe area spacer for iOS home indicator */}
      <div className="h-safe-area-inset-bottom" style={{ height: 'env(safe-area-inset-bottom)' }} />
    </nav>
  )
}
